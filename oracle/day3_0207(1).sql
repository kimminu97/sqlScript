--데이터 타입 number 연습 테이블
--number : 자바 byte,short,int,long,float,double 해당
--			number(전체자리수, 소수점자리수)
--table_number 테이블 이름
CREATE TABLE table_number(
	col1 NUMBER,		-- 자릿수를 지정하지 않으면 최대 38자리 입니다.
	col2 number(5),
	col3 number(7,2),
	col4 number(2,5)
	);
	
-- 정상 실행
INSERT INTO TABLE_NUMBER(COL1, COL2, COL3, COL4)
VALUES(1234567, 12345, 12345.67, 0.00012);

--col2 칼럼의 자릿수가 6개 : 오류
INSERT INTO TABLE_NUMBER(COL1, COL2, COL3, COL4)
VALUES(1234567, 123456, 12345.67, 0.00012);
--col3 칼럼은 소수점 이하 자리수를 2개 : 반올림
INSERT INTO TABLE_NUMBER(COL1, COL2, COL3, COL4)
VALUES(1234567, 12345, 12345.678, 0.00012);
--col3 컬럼 전체 자리수 7개 : 소수점이하 항상 2개, 그리고 정수부 5개여야하므로 오류
INSERT INTO TABLE_NUMBER(COL1, COL2, COL3, COL4)
VALUES(1234567, 12345, 12345.67, 0.00012);
--col4 컬럼은 number(2,5) : 유효자리수 2개(0포함가능), 나머지 3개는 유효자리수0
INSERT INTO TABLE_NUMBER(COL1, COL2, COL3, COL4)
VALUES(1234567, 12345, 12345.67, 0.000126);
--col4 컬럼은 number(2,5) :나머지 3개는 유효자리수 0 -> 이것이 아니면 오류
INSERT INTO TABLE_NUMBER(COL1, COL2, COL3, COL4)
VALUES(1234567, 12345, 12345.67, 0.0012);

--0.00012300 일 때, 유효한 숫자 0.000123 123뒤에 00은 유효한 값이 아닙니다.
